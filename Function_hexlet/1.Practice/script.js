// //Реализуйте функцию, которая проверяет переданное число на простоту
// //и печатает на экран yes или no.

let sayPrimeOrNot = (N) =>{
    let Yes = 'Yes', No = 'No'; //создаю переменные чтобы не использовать console.log();
if ( N < 1 ) return console.log('No');//сразу делаю проверку, что если число меньше 1, то оно не простое
    for ( var i = 2; i < N; i++ ){//создаю цилк, который будет начинатся с 2 и заканчиватся задданым числом
        if(N % i === 0){// проверяю, если число делится без остатка, то значит оно сложное
            return No;
        }// если же таких числе нет, то оно простое
    }
    return Yes;
}
console.log(sayPrimeOrNot(5));
console.log(sayPrimeOrNot(4));


