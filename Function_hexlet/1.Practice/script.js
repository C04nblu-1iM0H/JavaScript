//Реализуйте функцию, которая возвращает среднее арифметическое всех переданных аргументов.
//Если функции не передать ни одного аргумента, то она должна вернуть null.

function average(...params) {//использую в аргументе spred-параметр
    let ln = params.length,//записываю в новую переменную длину массива
    sum = 0, result = 0; 
    if(params.length === 0 ) return null;//если аргументов нет, то вывожу null
    for (let item of params) {//при помощи цилка for of перебираю массив 
        sum+=item;//складываю элементы
    }
    result = sum/ln;//полученный результат сложение делю на количество элементов по формуле
    return result; 
}

console.log(average(0));              // 0
console.log(average(0, 10));         // 5
console.log(average(-3, 4, 2, 10)); // 3.25
console.log(average());            // null

